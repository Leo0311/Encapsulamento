package atividade1;

public class Atividade1 {
private int idade  ;
private float peso;
private double pesoDois;
private char letra;
private boolean casado;
private long numeroGrande;
private short numeroCurto;
private byte numero ;

public int getIdade() {
	return idade;
}
public void setIdade(int idade) {
	this.idade = idade;
}
public float getPeso() {
	return peso;
}
public void setPeso(float peso) {
	this.peso = peso;
}
public double getpesoDois() {
	return pesoDois;
}
public void setpesoDois(double pesoDois) {
	this.pesoDois = pesoDois;
}
public char getLetra() {
	return letra;
}
public void setLetra(char letra) {
	this.letra = letra;
}
public boolean isCasado() {
	return casado;
}
public void setCasado(boolean casado) {
	this.casado = casado;
}
public long getnumeroGrande() {
	return numeroGrande;
}
public void setnumeroGrande(long numeroGrande) {
	this.numeroGrande = numeroGrande;
}
public short getNumeroCurto() {
	return numeroCurto;
}
public void setNumeroCurto(short numeroCurto) {
	this.numeroCurto = numeroCurto;
}
public byte getnumero() {
	return numero;
}
public void setnumero(byte numero) {
	this.numero = numero;
}
}
